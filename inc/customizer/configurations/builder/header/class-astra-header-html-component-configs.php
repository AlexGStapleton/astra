/**
 * Register Builder Customizer Configurations.
 *
 * @since x.x.x
 */
class Astra_Header_Html_Component_Configs extends Astra_Customizer_Config_Base {

	/**
	 * Register Builder Customizer Configurations.
	 *
	 * @param Array                $configurations Astra Customizer Configurations.
	 * @param WP_Customize_Manager $wp_customize instance of WP_Customize_Manager.
	 * @since x.x.x
	 * @return Array Astra Customizer Configurations with updated configurations.
	 */
	public function register_configuration( $configurations, $wp_customize ) {

		$configurations = Astra_Html_Component_Configs::register_configuration( 'header', 'section-hb-html-', $configurations );

		return $configurations;
	}
}

/**
 * Kicking this off by creating object of this class.
 */

new Astra_Header_Html_Component_Configs();
